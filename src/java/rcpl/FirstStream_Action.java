package rcpl;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import org.apache.struts.action.ActionForm;
import org.apache.struts.action.ActionForward;
import org.apache.struts.action.ActionMapping;

/**
 *
 * @author ASHUTOSH
 */
public class FirstStream_Action extends org.apache.struts.action.Action {

    /* forward name="success" path="" */
    private static String SUCCESS = "success";

    /**
     * This is the action called from the Struts framework.
     *
     * @param mapping The ActionMapping used to select this instance.
     * @param form The optional ActionForm bean for this request.
     * @param request The HTTP Request we are processing.
     * @param response The HTTP Response we are processing.
     * @throws java.lang.Exception
     * @return
     */
    @Override
    public ActionForward execute(ActionMapping mapping, ActionForm form,
            HttpServletRequest request, HttpServletResponse response)
            throws Exception {
        FirstStream_Bean fsb=(FirstStream_Bean)form;
        String str1=fsb.getSelect0();
        if(str1.equals("Engineering"))
        {
            return mapping.findForward("E");
        }
        if(str1.equals("Medical"))
        {
            return mapping.findForward("M");
        }
        return mapping.findForward(SUCCESS);
    }
}
